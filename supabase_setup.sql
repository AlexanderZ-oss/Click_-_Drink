-- Enable Extensions
create extension if not exists "uuid-ossp";

-- 1. PROMOTIONS TABLE
create table if not exists public.promotions (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  title text not null,
  description text,
  discount_percent numeric,
  image_url text,
  active boolean default true,
  valid_until timestamp with time zone
);

-- 2. REVIEWS TABLE
create table if not exists public.reviews (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  user_id uuid references auth.users not null,
  user_name text,
  user_avatar text,
  rating integer check (rating >= 1 and rating <= 5),
  comment text,
  product_id bigint references public.products(id) on delete cascade
);

-- 3. CONTACT MESSAGES
create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text,
  email text,
  subject text,
  message text,
  read boolean default false
);

-- 4. UPDATE ORDERS (More details)
alter table public.orders add column if not exists invoice_type text default 'boleta'; -- boleta or factura
alter table public.orders add column if not exists ruc_dni text;
alter table public.orders add column if not exists company_name text;

-- Security Policies Refinement
alter table public.promotions enable row level security;
create policy "Promotions viewable by all" on promotions for select using (true);
create policy "Admins can manage promotions" on promotions for all using (exists (select 1 from profiles where id = auth.uid() and role = 'admin'));

alter table public.reviews enable row level security;
create policy "Reviews viewable by all" on reviews for select using (true);
create policy "Users can insert reviews" on reviews for insert with check (auth.uid() is not null);
create policy "Users can manage own reviews" on reviews for all using (auth.uid() = user_id);

alter table public.messages enable row level security;
create policy "Admins can view messages" on messages for select using (exists (select 1 from profiles where id = auth.uid() and role = 'admin'));
create policy "Anyone can send message" on messages for insert with check (true);

